    private void phraseTextAreaUpdate(javax.swing.event.DocumentEvent evt) {
        if (selectedNode != null && !selectedNode.equals(nodeClass))
            ((Node.ExplanationPhrase)phrasesMap.get(selectedNode)).strPhrase = phraseTextArea.getText();
    }
    
    private void descriptionTextAreaUpdate(javax.swing.event.DocumentEvent evt) {
        if (selectedNode != null)
            selectedNode.setExplanationDescription(descriptionTextArea.getText());
    }
    
    private void networkPanelMouseClicked(java.awt.event.MouseEvent evt) {
        selectedNode = (Node)netExplanationGraph.getSelected();
        boolean nodeIsSelected = selectedNode != null;
        boolean nodeIsNotClass = nodeIsSelected ? !selectedNode.equals(nodeClass) : true;
        Node.ExplanationPhrase phrase = ((Node.ExplanationPhrase)phrasesMap.get(selectedNode));

        descriptionTextArea.setText(nodeIsSelected ? selectedNode.getExplanationDescription() : "");
        phraseTextArea.setText((nodeIsSelected && nodeIsNotClass) ? phrase.strPhrase : "");
        triggerTypeRadioButton.setSelected((nodeIsSelected && nodeIsNotClass) ? phrase.type.equals(phrase.TRIGGER_TYPE) : false);
        complementaryTypeRadioButton.setSelected((nodeIsSelected && nodeIsNotClass) ? phrase.type.equals(phrase.COMPLEMENTARY_TYPE) : false);
        naTypeRadioButton.setSelected((nodeIsSelected && nodeIsNotClass) ? phrase.type.equals(phrase.NA_TYPE) : true);
        necessaryTypeRadioButton.setSelected((nodeIsSelected && nodeIsNotClass) ? phrase.type.equals(phrase.NECESSARY_TYPE) : false);
        exclusiveTypeRadioButton.setSelected((nodeIsSelected && nodeIsNotClass) ? phrase.type.equals(phrase.EXCLUSIVE_TYPE) : false);

        descriptionLabel.setEnabled(nodeIsSelected);
        descriptionTextArea.setEnabled(nodeIsSelected);
        phraseLabel.setEnabled(nodeIsSelected && nodeIsNotClass);
        phraseTextArea.setEnabled(nodeIsSelected && nodeIsNotClass);
        typeLabel.setEnabled(nodeIsSelected && nodeIsNotClass);
        triggerTypeRadioButton.setEnabled(nodeIsSelected && nodeIsNotClass);
        complementaryTypeRadioButton.setEnabled(nodeIsSelected && nodeIsNotClass);
        naTypeRadioButton.setEnabled(nodeIsSelected && nodeIsNotClass);
        necessaryTypeRadioButton.setEnabled(nodeIsSelected && nodeIsNotClass);
        exclusiveTypeRadioButton.setEnabled(nodeIsSelected && nodeIsNotClass);
    }
    
