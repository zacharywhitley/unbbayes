    private void openNetFile(File selectedFile) {
        try {
            net = new NetIO().load(selectedFile);
            initializeNaiveNet(net);
            NetWindow netWindow = new NetWindow(net);
            networkPanel.add(netWindow.getJspGraph());
            netExplanationGraph = netWindow.getIGraph();
            netExplanationGraph.removeMouseMotionListener(((MouseMotionListener[])netExplanationGraph.getListeners(MouseMotionListener.class))[0]);
            netExplanationGraph.removeKeyListener(((KeyListener[])netExplanationGraph.getListeners(KeyListener.class))[0]);
            netExplanationGraph.addMouseListener(new java.awt.event.MouseAdapter() {
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    networkPanelMouseClicked(evt);
                }
            });
            statusLabel.setText("File opened successfully");
        }
        catch (Exception e) {
            net = null; nodeClass = null; phrasesMap = null;
            System.err.print(e.getMessage());
            e.printStackTrace();
            statusLabel.setText(e.getMessage());
        }
    }
    
    private void saveNetFile(File selectedFile) {
        (new NetIO()).save(selectedFile, net);
        statusLabel.setText("File saved successfully");
    }
    
    private void initializeNaiveNet(ProbabilisticNetwork naiveNet) throws Exception {
        NodeList nos = net.getNos();
        int nNos = nos.size(), count = 0;
        for (int i = 0; i < nNos; i++) {
            Node no = (Node)nos.get(i);
            if (no.getParents().size() == 0) {
                nodeClass = no;
                count++;
            }
        }
        if (count != 1)
            throw new Exception(NAIVE_ERR);
        phrasesMap = nodeClass.getPhrasesMap();
        for (int i = 0; i < nNos; i++) {
            Node no = (Node)nos.get(i);
            if (nodeClass.equals(no) || (no.getParents().size() == 1 && nodeClass.equals(no.getParents().get(0)))) {
                if (phrasesMap.get(no) == null)
                    phrasesMap.put(no, new Node.ExplanationPhrase());
            }
            else
                throw new Exception(NAIVE_ERR);
        }
    }
    
